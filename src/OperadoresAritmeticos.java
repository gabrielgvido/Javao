public class OperadoresAritmeticos {
    public static void main(String[] args) {
        int numero1 = 10;
        int numero2 = 5;
        float mediaNumero1Numero2 = (numero1 + numero2) / 2;
        float divisaoNumero1Numero2 = numero1 / numero2;
        float somaNumero1Numero2 = numero1 + numero2;
        float produtoNumero1Numero2 = numero1 % numero2;

        System.out.println(mediaNumero1Numero2);
        System.out.println(divisaoNumero1Numero2);
        System.out.println(somaNumero1Numero2);
        System.out.println(produtoNumero1Numero2);
    }
}
